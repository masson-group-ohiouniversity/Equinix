# ─── Kinetics-mode Monte Carlo: nuisance lives in `parsed` rather ─────
# than in a `params` dict.  Kinetics fits read initial concentrations
# from ``parsed["concentrations"]`` and the cell volume from
# ``parsed["volumes"]`` directly, so we copy + perturb those entries
# rather than the equilibrium ``params`` structure.


def _sample_nuisance_parsed(nominal_parsed: dict, specs: dict, rng) -> dict:
    """Return a copy of ``parsed`` with concentrations / volumes perturbed.

    Spec key syntax (string → relative σ):
        ``"conc:<name>"`` perturbs ``parsed["concentrations"][<name>]``
            (use the FULL name as stored in parsed — typically ending
            in "0", e.g. "G0").
        ``"vol:<name>"`` perturbs ``parsed["volumes"][<name>]``
            (typically "V0").

    The original ``parsed`` is never mutated — the dict layout is deep-
    copied down to the relevant subkeys.  All other entries (kinetics,
    equilibria, nmr, …) are shared by reference, which is safe because
    the fit functions do not mutate them either.
    """
    p = dict(nominal_parsed)
    if "concentrations" in p:
        p["concentrations"] = dict(p["concentrations"])
    if "volumes" in p:
        p["volumes"] = dict(p["volumes"])
    for key, sigma_rel in (specs or {}).items():
        try:
            sr = float(sigma_rel)
        except Exception:
            continue
        if sr <= 0:
            continue
        eps = float(rng.normal(0.0, sr))
        if key.startswith("conc:"):
            name = key.split(":", 1)[1]
            d = p.get("concentrations", {})
            if name in d:
                d[name] = max(1e-12, float(d[name]) * (1.0 + eps))
        elif key.startswith("vol:"):
            name = key.split(":", 1)[1]
            d = p.get("volumes", {})
            if name in d:
                d[name] = max(1e-12, float(d[name]) * (1.0 + eps))
    return p


def _kin_finalise_names(fit_keys, fit_conc_keys, parsed):
    """Helper: build the names list and param_kinds dict for kinetics MC.

    Kinetics fit_keys can contain BOTH rate constants (linear display)
    and equilibrium constants (log display, sourced from
    ``parsed["equilibria"]``); we classify each entry here to match the
    jackknife unit-aware labeling convention.
    """
    fit_conc_keys = list(fit_conc_keys or [])
    _eq_knames = {eq.get("kname")
                   for eq in (parsed.get("equilibria") or [])
                   if isinstance(eq, dict) and eq.get("kname")}
    _log_keys      = [k for k in fit_keys if k in _eq_knames]
    _linear_k_keys = [k for k in fit_keys if k not in _eq_knames]
    names = list(fit_keys) + list(fit_conc_keys)
    kinds = _build_param_kinds(log_keys=_log_keys,
                                linear_k_keys=_linear_k_keys,
                                fit_conc_keys=fit_conc_keys)
    return names, kinds, _log_keys, _linear_k_keys


