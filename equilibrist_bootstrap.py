def bootstrap_spectra(parsed, network, spectra_data, params, logK_vals,
                      fit_keys, x_expr, *,
                      wl_min: float, wl_max: float,
                      n_bootstrap: int = 500,         # spectra are heavier
                      method: str = "residual",
                      alpha: float = 0.05,
                      n_jobs: int = 1,
                      seed: Optional[int] = None,
                      tolerance: float = 1e-6,
                      maxiter: int = 100_000,
                      auto_range: bool = False,
                      timeout_s: float = 30.0,
                      constraints=None,
                      fit_conc_keys=None,
                      fit_titrant_keys=None,
                      allow_negative_eps: bool = False,
                      progress_callback: Optional[Callable[[int, int], None]] = None
                      ) -> dict:
    """Bootstrap CIs for the UV-Vis / fluorescence spectra fit.

    Residuals are sampled from the 2-D residual matrix (n_spectra ×
    n_wavelengths).  Default ``n_bootstrap=500`` because each spectra
    refit is heavier than a concentration fit; 1000 iterations is
    typically affordable when ``n_jobs > 1``.
    """
    from equilibrist_fit_spectra import fit_spectra

    t0 = time.perf_counter()
    fit_conc_keys    = list(fit_conc_keys    or [])
    fit_titrant_keys = list(fit_titrant_keys or [])

    ok, fitted_logKs, best_stats, msg = fit_spectra(
        parsed, network, spectra_data, params, logK_vals, fit_keys, x_expr,
        wl_min, wl_max, tolerance, maxiter,
        auto_range=auto_range, timeout_s=timeout_s,
        constraints=constraints,
        fit_conc_keys=fit_conc_keys, fit_titrant_keys=fit_titrant_keys,
        allow_negative_eps=allow_negative_eps,
    )
    if not ok:
        return {"samples": {}, "ci": {}, "ci_levels": (alpha/2, 0.5, 1-alpha/2),
                "method": method, "n_bootstrap": n_bootstrap,
                "n_success": 0, "n_failed": n_bootstrap,
                "best_fit": {}, "best_stats": best_stats,
                "wall_seconds": 0.0, "best_message": msg}

    best_fit = dict(fitted_logKs)
    best_fit.update(best_stats.get("fitted_concs",    {}) or {})
    best_fit.update(best_stats.get("fitted_titrants", {}) or {})
    all_param_names = (list(fit_keys) + list(fit_conc_keys)
                       + list(fit_titrant_keys))

    # The spectra fit does not store A_obs/A_calc in stats by default
    # (the integration patch in app.py adds them).  We work from the
    # ravelled residuals when available.
    A_obs  = best_stats.get("A_obs")
    A_calc = best_stats.get("A_calc")
    if A_obs is None or A_calc is None:
        return {"samples": {}, "ci": {}, "ci_levels": (alpha/2, 0.5, 1-alpha/2),
                "method": method, "n_bootstrap": n_bootstrap,
                "n_success": 0, "n_failed": n_bootstrap,
                "best_fit": best_fit, "best_stats": best_stats,
                "wall_seconds": time.perf_counter() - t0,
                "best_message": ("Apply the spectra integration patch to "
                                 "fit_spectra so stats['A_obs'] and "
                                 "stats['A_calc'] are populated; "
                                 "bootstrap requires the residual matrix.")}

    A_obs  = np.asarray(A_obs,  dtype=float)
    A_calc = np.asarray(A_calc, dtype=float)
    residual_matrix = A_obs - A_calc
    shape = A_obs.shape

    def _worker(i, rng):
        try:
            flat_r = residual_matrix.ravel()
            r_synth = _resample_residuals(flat_r, method, rng).reshape(shape)
            A_synth = A_calc + r_synth
            # Build synthetic spectra_data structure: copy and overwrite
            # the absorbance matrix.  spectra_data layout is implementation-
            # dependent; we expect at minimum a key 'A_obs' or equivalent.
            synth = dict(spectra_data)
            # Common keys to try in order
            for k_try in ("A_obs", "A", "matrix", "abs_matrix"):
                if k_try in synth and isinstance(synth[k_try], np.ndarray):
                    synth[k_try] = A_synth
                    break
            ok_b, logKs_b, stats_b, _ = fit_spectra(
                parsed, network, synth, params, logK_vals, fit_keys, x_expr,
                wl_min, wl_max, tolerance, maxiter,
                auto_range=False,            # use the locked range from pass 1
                timeout_s=timeout_s,
                constraints=constraints,
                fit_conc_keys=fit_conc_keys, fit_titrant_keys=fit_titrant_keys,
                allow_negative_eps=allow_negative_eps,
                compute_hessian=False,
            )
            if not ok_b:
                return None
            out = dict(logKs_b)
            out.update(stats_b.get("fitted_concs",    {}) or {})
            out.update(stats_b.get("fitted_titrants", {}) or {})
            return out
        except Exception:
            return None

    seed_seq = np.random.SeedSequence(seed)
    raw = _run_iter(_worker, (), n_bootstrap, n_jobs, progress_callback, seed_seq)
    samples, n_ok, n_fail = _finalise_samples(raw, all_param_names)
    ci_levels = (alpha/2, 0.5, 1 - alpha/2)

    return {
        "samples":     samples,
        "ci":          _ci_block(samples, alpha),
        "bs_se":       _se_block(samples),
        "ci_levels":   ci_levels,
        "method":      method,
        "n_bootstrap": n_bootstrap,
        "n_success":   n_ok,
        "n_failed":    n_fail,
        "best_fit":    best_fit,
        "best_stats":  best_stats,
        "wall_seconds": time.perf_counter() - t0,
        "best_message": msg,
    }


