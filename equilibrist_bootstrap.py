# ════════════════════════════════════════════════════════════════════════
# Nuisance-parameter Monte Carlo — error propagation from experimental
# quantities that the optimizer treats as "known": initial concentrations
# of host/guest, titrant stock concentrations, and the initial volume V₀.
# ════════════════════════════════════════════════════════════════════════
#
# Motivation: a typical Equilibrist fit assumes the *nominal* values of
# conc0, V₀, and titrant stocks are exactly correct.  In practice each
# carries a gravimetric / volumetric uncertainty (~1–3 % is typical for
# carefully prepared NMR samples).  None of these enter the residuals,
# so the Hessian SE and the residual-resampling bootstrap are blind to
# them — they only quantify how the FITTED CONSTANTS respond to NOISE
# in the observed signals.  The reported precision is correspondingly
# optimistic.
#
# Nuisance-parameter MC fills the gap.  For each MC iteration:
#   1. Sample each user-flagged nuisance value from a Normal centred at
#      its nominal value, with σ = (nominal × user-specified relative %).
#   2. Build a perturbed ``params`` dict carrying the sampled values.
#   3. Re-run the standard fit using the SAME observed data but with the
#      perturbed (assumed-known) nuisance values.
#   4. Record the resulting fitted constants.
#
# The MC σ on each fitted constant tells the user how much of their
# final uncertainty is attributable to the experimental-prep uncertainty
# — a contribution that can be larger than the residual-noise component
# for well-fit data.  Comparing MC σ to bootstrap σ reveals which is
# dominant and where to invest experimental effort.


def _sample_nuisance_params(nominal: dict, specs: dict, rng) -> dict:
    """Return a deep-enough copy of ``nominal`` with each flagged entry
    multiplicatively perturbed by ``Normal(1, σ_rel)``.

    Spec key syntax (string → relative σ):
        ``"conc0:<name>"``    perturbs ``nominal["conc0"][<name>]``
        ``"titrant:<name>"``  perturbs ``nominal["titrant_mMs"][<name>]``
        ``"V0_mL"``           perturbs ``nominal["V0_mL"]``

    Sample values are clipped to a tiny positive floor (1e-12) so that
    a freak 5σ draw can't produce a non-physical negative concentration.
    Spec entries with ``σ_rel ≤ 0`` are silently skipped (= source not
    flagged for sampling), so the same UI can leave individual sources
    uncertainty-free without conditional code at the call site.
    """
    p = dict(nominal)
    if "conc0" in p:
        p["conc0"] = dict(p["conc0"])
    if "titrant_mMs" in p:
        p["titrant_mMs"] = dict(p["titrant_mMs"])
    for key, sigma_rel in (specs or {}).items():
        try:
            sr = float(sigma_rel)
        except Exception:
            continue
        if sr <= 0:
            continue
        eps = float(rng.normal(0.0, sr))
        if key.startswith("conc0:"):
            name = key.split(":", 1)[1]
            d = p.get("conc0", {})
            if name in d:
                d[name] = max(1e-12, float(d[name]) * (1.0 + eps))
        elif key.startswith("titrant:"):
            name = key.split(":", 1)[1]
            d = p.get("titrant_mMs", {})
            if name in d:
                d[name] = max(1e-12, float(d[name]) * (1.0 + eps))
        elif key == "V0_mL":
            if "V0_mL" in p:
                p["V0_mL"] = max(1e-12, float(p["V0_mL"]) * (1.0 + eps))
    return p


def _mc_empty(msg: str = "") -> dict:
    """Empty Monte Carlo result returned when the baseline fit fails."""
    return {"samples": {}, "mc_se": {}, "mc_median": {},
            "mc_ci_lo": {}, "mc_ci_hi": {},
            "best_fit": {}, "param_kinds": {},
            "nuisance_specs": {}, "n_iter": 0,
            "n_success": 0, "n_failed": 0,
            "wall_seconds": 0.0, "best_message": msg}


def _mc_finalise(raw_results, all_param_names) -> tuple:
    """Aggregate MC worker outputs.  Returns (samples, mc_se, mc_median,
    mc_ci_lo, mc_ci_hi, n_ok, n_fail).

    σ is the sample standard deviation with ddof=1 (each MC iteration
    is an independent draw from the parameter distribution, so this is
    a direct frequentist estimate — no jackknife sqrt(N−1) inflation).
    """
    samples, n_ok, n_fail = _finalise_samples(raw_results, all_param_names)
    mc_se      = {}
    mc_median  = {}
    mc_ci_lo   = {}
    mc_ci_hi   = {}
    for name in all_param_names:
        s = np.asarray(samples.get(name, []), dtype=float)
        s_v = s[np.isfinite(s)]
        if len(s_v) >= 2:
            mc_se[name]     = float(np.std(s_v, ddof=1))
            mc_median[name] = float(np.median(s_v))
        else:
            mc_se[name]     = float("nan")
            mc_median[name] = float("nan")
        if len(s_v) >= 10:
            mc_ci_lo[name] = float(np.quantile(s_v, 0.025))
            mc_ci_hi[name] = float(np.quantile(s_v, 0.975))
        else:
            mc_ci_lo[name] = float("nan")
            mc_ci_hi[name] = float("nan")
    return samples, mc_se, mc_median, mc_ci_lo, mc_ci_hi, n_ok, n_fail


