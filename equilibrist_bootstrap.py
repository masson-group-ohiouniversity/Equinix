def bootstrap_kinetics_spectra(parsed, logk_dict, spectra_data, fit_keys, *,
                               t_max: float, n_pts: int,
                               wl_min: float, wl_max: float,
                               n_bootstrap: int = 500,
                               method: str = "residual",
                               alpha: float = 0.05,
                               n_jobs: int = 1,
                               seed: Optional[int] = None,
                               tolerance: float = 1e-6,
                               maxiter: int = 100_000,
                               timeout_s: float = 30.0,
                               constraints=None,
                               fit_conc_keys=None,
                               allow_negative_eps: bool = False,
                               progress_callback: Optional[Callable[[int, int], None]] = None
                               ) -> dict:
    """Bootstrap CIs for kinetics + UV-Vis spectra fit.

    Uses the flat y_obs/y_calc arrays (raveled absorbance matrices) from
    the v2-patched fit_kinetics_spectra stats dict.  Resamples residuals
    once across the full flat array and injects the synthetic absorbance
    matrix back into spectra_data.
    """
    from equilibrist_kinetics_spectra import fit_kinetics_spectra

    t0 = time.perf_counter()
    fit_conc_keys = list(fit_conc_keys or [])

    ok, fitted_logks, best_stats, msg = fit_kinetics_spectra(
        parsed, logk_dict, spectra_data, fit_keys,
        t_max, n_pts, wl_min, wl_max, tolerance, maxiter,
        timeout_s=timeout_s, constraints=constraints,
        fit_conc_keys=fit_conc_keys,
        allow_negative_eps=allow_negative_eps,
    )
    if not ok:
        return {"samples": {}, "ci": {}, "ci_levels": (alpha/2, 0.5, 1-alpha/2),
                "method": method, "n_bootstrap": n_bootstrap,
                "n_success": 0, "n_failed": n_bootstrap,
                "best_fit": {}, "best_stats": best_stats,
                "wall_seconds": 0.0, "best_message": msg}

    best_fit = dict(fitted_logks)
    best_fit.update(best_stats.get("fitted_concs", {}) or {})
    all_param_names = (list(fit_keys) + list(fit_conc_keys))

    A_obs  = best_stats.get("A_obs")
    A_calc = best_stats.get("A_calc")
    if A_obs is None or A_calc is None:
        return {"samples": {}, "ci": {}, "ci_levels": (alpha/2, 0.5, 1-alpha/2),
                "method": method, "n_bootstrap": n_bootstrap,
                "n_success": 0, "n_failed": n_bootstrap,
                "best_fit": best_fit, "best_stats": best_stats,
                "wall_seconds": time.perf_counter() - t0,
                "best_message": "A_obs / A_calc unavailable in stats"}
    A_obs  = np.asarray(A_obs,  dtype=float)
    A_calc = np.asarray(A_calc, dtype=float)
    residuals_flat = (A_obs - A_calc).ravel()

    # The fit reads spectra_data['A'] (n_t × n_wl_full); A_obs is the
    # wavelength-cropped subset.  We need to inject synthetic absorbances
    # into the *full* A matrix at the wavelengths used by the fit.
    wl_full   = np.asarray(spectra_data.get("wavelengths", []), dtype=float)
    wl_fit    = np.asarray(best_stats.get("wavelengths_fit", []), dtype=float)
    if len(wl_full) == 0 or len(wl_fit) == 0:
        return {"samples": {}, "ci": {}, "ci_levels": (alpha/2, 0.5, 1-alpha/2),
                "method": method, "n_bootstrap": n_bootstrap,
                "n_success": 0, "n_failed": n_bootstrap,
                "best_fit": best_fit, "best_stats": best_stats,
                "wall_seconds": time.perf_counter() - t0,
                "best_message": "Wavelength grids unavailable for injection"}
    wl_mask = np.isin(wl_full, wl_fit)

    def _worker(i, rng):
        try:
            r_synth = _resample_residuals(residuals_flat, method, rng)
            A_synth_fit = A_calc + r_synth.reshape(A_calc.shape)
            new_data = dict(spectra_data)
            A_full = np.asarray(spectra_data["A"], dtype=float).copy()
            A_full[:, wl_mask] = A_synth_fit
            new_data["A"] = A_full
            ok_b, logks_b, stats_b, _ = fit_kinetics_spectra(
                parsed, logk_dict, new_data, fit_keys,
                t_max, n_pts, wl_min, wl_max, tolerance, maxiter,
                timeout_s=timeout_s, constraints=constraints,
                fit_conc_keys=fit_conc_keys,
                allow_negative_eps=allow_negative_eps,
                compute_hessian=False,
            )
            if not ok_b:
                return None
            out = dict(logks_b)
            out.update(stats_b.get("fitted_concs", {}) or {})
            return out
        except Exception:
            return None

    seed_seq = np.random.SeedSequence(seed)
    raw = _run_iter(_worker, (), n_bootstrap, n_jobs, progress_callback, seed_seq)
    samples, n_ok, n_fail = _finalise_samples(raw, all_param_names)
    ci_levels = (alpha/2, 0.5, 1 - alpha/2)
    return {
        "samples":     samples,
        "ci":          _ci_block(samples, alpha),
        "bs_se":       _se_block(samples),
        "ci_levels":   ci_levels,
        "method":      method,
        "n_bootstrap": n_bootstrap,
        "n_success":   n_ok,
        "n_failed":    n_fail,
        "best_fit":    best_fit,
        "best_stats":  best_stats,
        "wall_seconds": time.perf_counter() - t0,
        "best_message": msg,
    }


# ════════════════════════════════════════════════════════════════════════
# Jackknife (leave-one-out) analysis — per titration step
# ════════════════════════════════════════════════════════════════════════
#
# Rationale for the choice of "jackknife unit" (per-x-value, not
# per-observation):  In a titration with multiple signal columns
# (e.g. four NMR shifts tracked at each titration step) the residuals
# at the same step share the same underlying chemistry — they reflect
# one [H], [G], [HG], [HG₂] tuple.  Dropping a single (signal, row)
# pair leaves the remaining signals at that step still informing the
# fit, so the perturbation is artificially small (≈ 1 / N_signals of
# the chemistry content at that step).
#
# Removing the entire row across all columns — i.e. all four signals
# at row 5 — is the natural "leave one observation set out": it deletes
# every observation at one chemistry condition, the perturbation is
# meaningful, and the resulting per-step influence Δᵢ is interpretable
# as "how much does the fit move if this experimental point is dropped
# entirely?".  Same logic in spectra (drop one full spectrum, not one
# pixel) and kinetics (drop one time slice across all observed
# species).  SupraFit and SIVVU both jackknife at this granularity.

# ─── Generic helpers (shared by all jackknife_<mode> functions) ──────


def _enumerate_steps_column_dict(data_dict: dict,
                                  per_col_x_plot: dict | None = None
                                  ) -> list:
    """Return ``[(row_idx, x_display, n_obs), …]`` for dict-of-columns data.

    Each emitted entry is one titration step: a row index that is
    valid in at least one signal column, the x-coordinate it should
    be plotted at (in the user's plot units, e.g. H0/G0), and the
    number of observations that will be dropped from that step.
    """
    if not isinstance(data_dict, dict):
        return []
    per_col_x_plot = per_col_x_plot or {}
    cols = [c for c, cd in data_dict.items()
            if not (isinstance(c, str) and c.startswith("_"))
            and isinstance(cd, dict)
            and "v_add_mL" in cd and "y" in cd]
    if not cols:
        return []

    # Maximum row index that appears in any signal column
    max_rows = 0
    for c in cols:
        v = np.asarray(data_dict[c].get("v_add_mL", []), dtype=float)
        if len(v) > max_rows:
            max_rows = len(v)

    # Canonical column for the x-axis display value: the first one
    # that has a per_col_x entry, falling back to first column.
    canon_col = next((c for c in cols if c in per_col_x_plot), cols[0])
    canon_v = np.asarray(data_dict[canon_col].get("v_add_mL", []), dtype=float)
    canon_x_plot = np.asarray(per_col_x_plot.get(canon_col, []), dtype=float)

    # Walk rows, counting finite observations across columns and
    # mapping the row to its display-x value via the canonical
    # column's per_col_x array (which only lists finite points,
    # so we keep a separate counter).
    finite_canon = 0
    steps = []
    for i in range(max_rows):
        n_obs = 0
        for c in cols:
            v = np.asarray(data_dict[c].get("v_add_mL", []), dtype=float)
            y = np.asarray(data_dict[c].get("y", []), dtype=float)
            if (i < len(v) and i < len(y)
                    and np.isfinite(v[i]) and np.isfinite(y[i])):
                n_obs += 1
        if n_obs == 0:
            continue
        # x display value
        canon_row_finite = (i < len(canon_v)
                             and np.isfinite(canon_v[i]))
        if canon_row_finite and finite_canon < len(canon_x_plot):
            x_disp = float(canon_x_plot[finite_canon])
        elif i < len(canon_v) and np.isfinite(canon_v[i]):
            x_disp = float(canon_v[i])
        else:
            x_disp = float("nan")
        if canon_row_finite:
            finite_canon += 1
        steps.append((int(i), x_disp, int(n_obs)))
    return steps


def _drop_row_from_column_dict(data_dict: dict, row_idx: int) -> dict:
    """Return shallow copy of ``data_dict`` with ``row_idx`` removed
    from every signal column.  Keys starting with ``_`` are kept as-is."""
    synth = {}
    for col, col_data in data_dict.items():
        if isinstance(col, str) and col.startswith("_"):
            synth[col] = col_data; continue
        if not isinstance(col_data, dict):
            synth[col] = col_data; continue
        v_full = col_data.get("v_add_mL", [])
        y_full = col_data.get("y",        [])
        v = np.asarray(v_full, dtype=float)
        y = np.asarray(y_full, dtype=float)
        n_c = min(len(v), len(y))
        if row_idx < n_c:
            mask = np.ones(n_c, dtype=bool)
            mask[row_idx] = False
            synth[col] = {
                **col_data,
                "v_add_mL": v[:n_c][mask],
                "y":        y[:n_c][mask],
            }
        else:
            synth[col] = col_data
    return synth


def _enumerate_steps_spectra(spectra_data: dict,
                              x_plot_arr=None) -> list:
    """Return ``[(row_idx, x_display, n_obs), …]`` for spectra-style data.

    Each step is one spectrum (one ``x_vals`` row of the absorbance
    matrix ``A``).  ``n_obs`` is the number of finite wavelength
    entries in that row — purely informational for the UI.

    Note: spectra modules use ``x_vals`` (raw x, e.g. volume of titrant
    added or time in seconds) as their row index, NOT ``v_add_mL`` like
    the column-dict NMR/CONC layouts.  ``x_plot_arr`` is the converted
    plot-coordinate array (``best_stats["x_exp"]``) of the same length.
    """
    v = np.asarray(spectra_data.get("x_vals", []), dtype=float)
    A = np.asarray(spectra_data.get("A", []), dtype=float)
    if A.ndim != 2 or len(v) == 0:
        return []
    n_rows = min(len(v), A.shape[0])
    x_arr = np.asarray(x_plot_arr if x_plot_arr is not None else [],
                       dtype=float)
    steps = []
    for i in range(n_rows):
        if not np.isfinite(v[i]):
            continue
        n_obs = int(np.sum(np.isfinite(A[i, :])))
        if n_obs == 0:
            continue
        x_disp = (float(x_arr[i])
                   if i < len(x_arr) and np.isfinite(x_arr[i])
                   else float(v[i]))
        steps.append((int(i), x_disp, n_obs))
    return steps


def _drop_row_from_spectra(spectra_data: dict, row_idx: int) -> dict:
    """Return shallow copy of ``spectra_data`` with one spectrum row
    removed from both ``x_vals`` and the absorbance matrix ``A``."""
    v = np.asarray(spectra_data.get("x_vals", []), dtype=float)
    A = np.asarray(spectra_data.get("A", []), dtype=float)
    n_c = min(len(v), A.shape[0]) if A.ndim == 2 else 0
    if row_idx >= n_c:
        return spectra_data
    mask = np.ones(n_c, dtype=bool)
    mask[row_idx] = False
    synth = dict(spectra_data)
    synth["x_vals"] = v[:n_c][mask]
    synth["A"]      = A[:n_c, :][mask]
    return synth


def _jackknife_finalise(raw_results, all_param_names, full_param_dict):
    """Compute jackknife SE + per-step influence from worker outputs."""
    samples, n_ok, n_fail = _finalise_samples(raw_results, all_param_names)
    jack_se   = {}
    influence = {}
    for name in all_param_names:
        full_v = float(full_param_dict.get(name, float("nan")))
        s = np.asarray(samples.get(name, []), dtype=float)
        influence[name] = full_v - s
        s_valid = s[np.isfinite(s)]
        if len(s_valid) >= 2:
            n_eff = len(s_valid)
            theta_bar = float(s_valid.mean())
            jack_se[name] = float(
                np.sqrt((n_eff - 1) / n_eff *
                         np.sum((s_valid - theta_bar) ** 2)))
        else:
            jack_se[name] = float("nan")
    return samples, jack_se, influence, n_ok, n_fail


def _build_param_kinds(log_keys=None, linear_k_keys=None,
                       fit_conc_keys=None, fit_titrant_keys=None) -> dict:
    """Return ``{param_name: "log" | "linear_k" | "linear_mM"}`` for jackknife display.

    Display-unit conventions, mirroring the main fitted-constants table:

    * ``log_keys``      → equilibrium constants.  Stored in log10 space
                          by the optimizer; chemists report them as
                          log K, so Δᵢ stays in log units.
    * ``linear_k_keys`` → rate constants.  Also optimized in log space,
                          but reported in *linear* units (s⁻¹, …) — the
                          kinetics jackknife functions therefore convert
                          them to linear before computing Δᵢ.
    * ``fit_conc_keys`` / ``fit_titrant_keys`` → fitted concentrations
                          and titrants.  Returned by the fit modules in
                          linear mM directly.

    Note: ``log_keys`` and ``linear_k_keys`` partition a fit's "fit_keys"
    according to the parsed script's ``equilibria`` set — a kinetic
    model that also fits an equilibrium constant (e.g. k_fwd / k_rev
    plus K_eq) will have entries in BOTH sets.
    """
    kinds = {}
    for k in (log_keys or []):         kinds[k] = "log"
    for k in (linear_k_keys or []):    kinds[k] = "linear_k"
    for k in (fit_conc_keys or []):    kinds[k] = "linear_mM"
    for k in (fit_titrant_keys or []): kinds[k] = "linear_mM"
    return kinds




def _jackknife_empty(msg: str = "") -> dict:
    """Empty jackknife result returned on baseline-fit failure."""
    return {"steps": [], "samples": {}, "influence": {}, "jack_se": {},
            "best_fit": {}, "param_kinds": {},
            "n_jack": 0, "n_success": 0, "n_failed": 0,
            "wall_seconds": 0.0, "best_message": msg}


