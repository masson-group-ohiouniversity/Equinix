def percentile_ci(samples: np.ndarray,
                  alpha: float = 0.05) -> tuple[float, float, float]:
    """Return (lo, median, hi) percentile CI from a sample array.

    ``alpha`` is the total tail probability — 0.05 gives a 95 % CI.
    """
    s = np.asarray(samples, dtype=float)
    s = s[np.isfinite(s)]
    if len(s) == 0:
        return (float("nan"), float("nan"), float("nan"))
    lo, med, hi = np.quantile(s, [alpha / 2, 0.5, 1 - alpha / 2])
    return float(lo), float(med), float(hi)


def _resample_residuals(residuals: np.ndarray,
                        method: str,
                        rng: np.random.Generator) -> np.ndarray:
    """Generate one bootstrap residual vector.

    Methods:
      * 'residual'   — sample with replacement from centred residuals
      * 'parametric' — draw N(0, σ̂) noise where σ̂ = std(residuals, ddof=1)
      * 'wild'       — Mammen two-point auxiliary variate (heteroscedastic)
    """
    r = np.asarray(residuals, dtype=float)
    r = r - r.mean()                                 # centre
    n = len(r)
    if method == "parametric":
        sigma = float(np.std(r, ddof=1)) if n > 1 else float(np.std(r))
        return sigma * rng.standard_normal(n)
    if method == "wild":
        # Mammen two-point: u takes value a w.p. p, and b w.p. 1-p,
        # where a = -(√5 - 1)/2, b = (√5 + 1)/2, p = (√5 + 1)/(2√5).
        a = -(np.sqrt(5.0) - 1.0) / 2.0
        b =  (np.sqrt(5.0) + 1.0) / 2.0
        p =  (np.sqrt(5.0) + 1.0) / (2.0 * np.sqrt(5.0))
        u = np.where(rng.random(n) < p, a, b)
        return r * u
    # default = 'residual' (non-parametric)
    idx = rng.integers(0, n, size=n)
    return r[idx]


def _run_iter(work_func: Callable, args: tuple,
              n_iter: int, n_jobs: int,
              progress_callback: Optional[Callable[[int, int], None]],
              seed_seq: np.random.SeedSequence):
    """Driver loop.  Calls ``work_func(iter_index, rng, *args)`` ``n_iter``
    times.  Returns a list of work_func return values (one per iteration,
    in submission order).  Failures are passed through; the caller filters.
    """
    if n_jobs == 1:
        results = []
        sub_seeds = seed_seq.spawn(n_iter)
        for i in range(n_iter):
            rng = np.random.default_rng(sub_seeds[i])
            results.append(work_func(i, rng, *args))
            if progress_callback is not None:
                try: progress_callback(i + 1, n_iter)
                except Exception: pass
        return results
    # Parallel via joblib
    try:
        from joblib import Parallel, delayed
    except Exception:
        # joblib unavailable — fall back to serial
        return _run_iter(work_func, args, n_iter, 1,
                         progress_callback, seed_seq)
    sub_seeds = seed_seq.spawn(n_iter)
    def _wrap(i, ss):
        return work_func(i, np.random.default_rng(ss), *args)
    # Try generator mode (joblib >= 1.3) so we can drive the progress bar
    # from the main process as workers complete tasks.  The callback runs
    # in the main process — the workers cannot reach Streamlit directly.
    try:
        gen = Parallel(n_jobs=n_jobs, prefer="processes",
                       return_as="generator")(
            delayed(_wrap)(i, sub_seeds[i]) for i in range(n_iter)
        )
        results = []
        for r in gen:
            results.append(r)
            if progress_callback is not None:
                try: progress_callback(len(results), n_iter)
                except Exception: pass
        return results
    except TypeError:
        # Old joblib without return_as — silent parallel batch
        return Parallel(n_jobs=n_jobs, prefer="processes",
                        batch_size="auto")(
            delayed(_wrap)(i, sub_seeds[i]) for i in range(n_iter)
        )


def _ci_block(samples_dict: dict, alpha: float) -> dict:
    """Run percentile_ci over every key in ``samples_dict``."""
    return {k: percentile_ci(v, alpha) for k, v in samples_dict.items()}


def _se_block(samples_dict: dict) -> dict:
    """Per-parameter bootstrap standard error.

    For each parameter, returns the sample standard deviation of the
    bootstrap distribution (ddof = 1), computed over the finite samples
    only.  This is the bootstrap analogue of the Hessian SE
    (``fit_result_summary["param_errors"]``), the jackknife SE
    (``jack_se``), and the Monte Carlo SE (``mc_se``), so every
    uncertainty-quantification family in Equilibrist exposes the same
    summary statistic alongside its native form (percentile CI for
    bootstrap, influence-and-SE for jackknife, etc.).

    Caveat for the chemist reader: for a heavy-tailed bootstrap
    distribution this single-number σ is a *summary*, not a sufficient
    statistic — the percentile CI carries the full distributional
    information and should remain the headline number whenever the
    Shapiro-Wilk test on the fit residuals rejects normality.
    """
    out: dict = {}
    for k, v in samples_dict.items():
        s = np.asarray(v, dtype=float)
        s = s[np.isfinite(s)]
        if s.size > 1:
            out[k] = float(np.std(s, ddof=1))
        else:
            out[k] = float("nan")
    return out


def _empty_samples(names: Iterable[str]) -> dict:
    return {n: [] for n in names}


def _finalise_samples(raw_results, all_param_names: list[str]) -> tuple[dict, int, int]:
    """Convert raw worker returns into a {param: ndarray} dict.

    Worker returns are either ``None`` (convergence failure) or
    ``dict[param_name -> float]``.  Missing params are treated as NaN
    so the percentile path stays well-defined.
    """
    samples = {n: [] for n in all_param_names}
    n_ok = 0
    n_fail = 0
    for r in raw_results:
        if r is None or not isinstance(r, dict):
            n_fail += 1
            continue
        n_ok += 1
        for n in all_param_names:
            samples[n].append(float(r.get(n, float("nan"))))
    samples = {n: np.asarray(v, dtype=float) for n, v in samples.items()}
    return samples, n_ok, n_fail


def _extract_per_col(best_stats: dict,
                     yobs_key: str = "per_col_y_obs",
                     ycalc_key: str = "per_col_y_calc") -> dict:
    """Build per-column {col: (residuals, y_obs, y_calc)} from v2 stats keys.

    Returns an empty dict if the stats dict lacks per-column arrays — caller
    should treat that as "bootstrap unsupported for this fit".
    """
    per_yobs  = best_stats.get(yobs_key)  or {}
    per_ycalc = best_stats.get(ycalc_key) or {}
    out = {}
    for col, y_obs_arr in per_yobs.items():
        y_calc_arr = per_ycalc.get(col)
        if y_calc_arr is None:
            continue
        y_obs_arr  = np.asarray(y_obs_arr,  dtype=float)
        y_calc_arr = np.asarray(y_calc_arr, dtype=float)
        mask = np.isfinite(y_obs_arr) & np.isfinite(y_calc_arr)
        if not mask.any():
            continue
        out[col] = ((y_obs_arr - y_calc_arr)[mask],
                    y_obs_arr[mask],
                    y_calc_arr[mask])
    return out


def _inject_synth_y(col_data: dict, y_synth_col: np.ndarray,
                    y_key: str = "y", x_key: str = "v_add_mL") -> dict:
    """Return a deep-ish copy of col_data with finite y-slots overwritten by y_synth_col.

    Preserves any non-finite entries in the original (NaNs etc.) so that
    downstream code that filters by finiteness still sees the same layout.
    """
    new_col = dict(col_data)
    v_arr_c = np.asarray(col_data.get(x_key, col_data.get("x", [])),
                         dtype=float)
    y_arr_c = np.asarray(col_data[y_key], dtype=float).copy()
    finite_idx = np.where(np.isfinite(v_arr_c) & np.isfinite(y_arr_c))[0]
    k_lim = min(len(finite_idx), len(y_synth_col))
    y_arr_c[finite_idx[:k_lim]] = y_synth_col[:k_lim]
    new_col[y_key] = y_arr_c
    return new_col


