def bootstrap_nmr_shift(parsed, network, nmr_data, params, logK_vals, fit_keys,
                        x_expr, *,
                        n_bootstrap: int = 1000,
                        method: str = "residual",
                        alpha: float = 0.05,
                        n_jobs: int = 1,
                        seed: Optional[int] = None,
                        tolerance: float = 1e-6,
                        maxiter: int = 100_000,
                        constraints=None,
                        fit_conc_keys=None,
                        fit_titrant_keys=None,
                        progress_callback: Optional[Callable[[int, int], None]] = None
                        ) -> dict:
    """Bootstrap CIs for the NMR shift fit (fast-exchange mode)."""
    from equilibrist_fit_nmr import fit_nmr_shifts

    t0 = time.perf_counter()
    fit_conc_keys    = list(fit_conc_keys    or [])
    fit_titrant_keys = list(fit_titrant_keys or [])

    ok, fitted_logKs, best_stats, msg = fit_nmr_shifts(
        parsed, network, nmr_data, params, logK_vals, fit_keys, x_expr,
        tolerance=tolerance, maxiter=maxiter, constraints=constraints,
        fit_conc_keys=fit_conc_keys, fit_titrant_keys=fit_titrant_keys,
    )
    if not ok:
        return {"samples": {}, "ci": {}, "ci_levels": (alpha/2, 0.5, 1-alpha/2),
                "method": method, "n_bootstrap": n_bootstrap,
                "n_success": 0, "n_failed": n_bootstrap,
                "best_fit": {}, "best_stats": best_stats,
                "wall_seconds": 0.0, "best_message": msg}

    best_fit = dict(fitted_logKs)
    best_fit.update(best_stats.get("fitted_concs",    {}) or {})
    best_fit.update(best_stats.get("fitted_titrants", {}) or {})
    all_param_names = (list(fit_keys) + list(fit_conc_keys)
                       + list(fit_titrant_keys))

    # Pull per-column residuals from the v2 stats dict.  Falls back to
    # reconstructing from delta_vecs_all / delta_bound_all for backward
    # compatibility with un-patched fit modules.
    per_col_yobs  = best_stats.get("per_col_y_obs")  or {}
    per_col_ycalc = best_stats.get("per_col_y_calc") or {}
    per_col_x_arr = best_stats.get("per_col_x")      or {}

    col_residuals: dict = {}
    col_xobs:      dict = {}
    col_yobs:      dict = {}
    col_ycalc:     dict = {}
    if per_col_yobs and per_col_ycalc:
        # Fast path — fit module already exposed per-column arrays.
        for col, y_obs_arr in per_col_yobs.items():
            y_calc_arr = per_col_ycalc.get(col)
            if y_calc_arr is None:
                continue
            x_arr = per_col_x_arr.get(col,
                                      np.arange(len(y_obs_arr), dtype=float))
            y_obs_arr  = np.asarray(y_obs_arr,  dtype=float)
            y_calc_arr = np.asarray(y_calc_arr, dtype=float)
            mask = np.isfinite(y_obs_arr) & np.isfinite(y_calc_arr)
            col_residuals[col] = (y_obs_arr - y_calc_arr)[mask]
            col_xobs[col]      = np.asarray(x_arr, dtype=float)[mask] if len(x_arr) == len(mask) else np.arange(int(mask.sum()), dtype=float)
            col_yobs[col]      = y_obs_arr[mask]
            col_ycalc[col]     = y_calc_arr[mask]
    else:
        # Legacy path — reconstruct from delta_vecs_all + delta_bound_all.
        delta_vecs  = best_stats.get("delta_vecs_all")  or {}
        delta_bound = best_stats.get("delta_bound_all") or {}
        for col, entry in delta_vecs.items():
            try:
                if not (isinstance(entry, tuple) and len(entry) >= 2):
                    continue
                x_arr, y_obs_arr = np.asarray(entry[0]), np.asarray(entry[1])
                calc_entry = delta_bound.get(col)
                if calc_entry is None:
                    continue
                if isinstance(calc_entry, tuple) and len(calc_entry) >= 2:
                    x_calc, y_calc_arr = (np.asarray(calc_entry[0]),
                                          np.asarray(calc_entry[1]))
                    if not (len(x_calc) == len(x_arr)
                            and np.allclose(x_calc, x_arr)):
                        order = np.argsort(x_calc)
                        y_calc_arr = np.interp(x_arr, x_calc[order],
                                               y_calc_arr[order])
                else:
                    y_calc_arr = np.asarray(calc_entry)
                mask = np.isfinite(x_arr) & np.isfinite(y_obs_arr) & np.isfinite(y_calc_arr)
                col_residuals[col] = (y_obs_arr - y_calc_arr)[mask]
                col_xobs[col]      = x_arr[mask]
                col_yobs[col]      = y_obs_arr[mask]
                col_ycalc[col]     = y_calc_arr[mask]
            except Exception:
                continue

    if not col_residuals:
        return {"samples": {}, "ci": {}, "ci_levels": (alpha/2, 0.5, 1-alpha/2),
                "method": method, "n_bootstrap": n_bootstrap,
                "n_success": 0, "n_failed": n_bootstrap,
                "best_fit": best_fit, "best_stats": best_stats,
                "wall_seconds": time.perf_counter() - t0,
                "best_message": "No residuals reconstructible from stats"}

    def _worker(i, rng):
        try:
            synth_nmr = {}
            for col, col_data in nmr_data.items():
                if col.startswith("_"):
                    synth_nmr[col] = col_data
                    continue
                if col not in col_residuals:
                    synth_nmr[col] = col_data
                    continue
                new_col = dict(col_data)
                r_synth = _resample_residuals(col_residuals[col], method, rng)
                y_synth_col = col_ycalc[col] + r_synth
                # Map back into the full column array preserving non-finite slots
                v_arr_c = np.asarray(col_data.get("v_add_mL",
                                                  col_data.get("x", [])), dtype=float)
                y_arr_c = np.asarray(col_data["y"], dtype=float).copy()
                finite_idx = np.where(np.isfinite(v_arr_c) & np.isfinite(y_arr_c))[0]
                k_lim = min(len(finite_idx), len(y_synth_col))
                y_arr_c[finite_idx[:k_lim]] = y_synth_col[:k_lim]
                new_col["y"] = y_arr_c
                synth_nmr[col] = new_col

            ok_b, logKs_b, stats_b, _ = fit_nmr_shifts(
                parsed, network, synth_nmr, params, logK_vals, fit_keys, x_expr,
                tolerance=tolerance, maxiter=maxiter, constraints=constraints,
                fit_conc_keys=fit_conc_keys, fit_titrant_keys=fit_titrant_keys,
                compute_hessian=False,
            )
            if not ok_b:
                return None
            out = dict(logKs_b)
            out.update(stats_b.get("fitted_concs",    {}) or {})
            out.update(stats_b.get("fitted_titrants", {}) or {})
            return out
        except Exception:
            return None

    seed_seq = np.random.SeedSequence(seed)
    raw = _run_iter(_worker, (), n_bootstrap, n_jobs, progress_callback, seed_seq)
    samples, n_ok, n_fail = _finalise_samples(raw, all_param_names)
    ci_levels = (alpha/2, 0.5, 1 - alpha/2)

    return {
        "samples":     samples,
        "ci":          _ci_block(samples, alpha),
        "bs_se":       _se_block(samples),
        "ci_levels":   ci_levels,
        "method":      method,
        "n_bootstrap": n_bootstrap,
        "n_success":   n_ok,
        "n_failed":    n_fail,
        "best_fit":    best_fit,
        "best_stats":  best_stats,
        "wall_seconds": time.perf_counter() - t0,
        "best_message": msg,
    }


