def bootstrap_nmr_integ(parsed, network, nmr_data, params, logK_vals, fit_keys,
                        x_expr, *,
                        n_bootstrap: int = 1000,
                        method: str = "residual",
                        alpha: float = 0.05,
                        n_jobs: int = 1,
                        seed: Optional[int] = None,
                        tolerance: float = 1e-6,
                        maxiter: int = 100_000,
                        constraints=None,
                        fit_conc_keys=None,
                        fit_titrant_keys=None,
                        progress_callback: Optional[Callable[[int, int], None]] = None
                        ) -> dict:
    """Bootstrap CIs for the NMR integration fit (slow-exchange mode).

    The slow-exchange residuals live in the integration columns of
    ``nmr_data``.  We reconstruct them by running the model forward once
    using the existing fit machinery and injecting per-column synthetic
    integrations.
    """
    from equilibrist_fit_nmr import fit_nmr_integration

    t0 = time.perf_counter()
    fit_conc_keys    = list(fit_conc_keys    or [])
    fit_titrant_keys = list(fit_titrant_keys or [])

    ok, fitted_logKs, best_stats, msg = fit_nmr_integration(
        parsed, network, nmr_data, params, logK_vals, fit_keys, x_expr,
        tolerance=tolerance, maxiter=maxiter, constraints=constraints,
        fit_conc_keys=fit_conc_keys, fit_titrant_keys=fit_titrant_keys,
    )
    if not ok:
        return {"samples": {}, "ci": {}, "ci_levels": (alpha/2, 0.5, 1-alpha/2),
                "method": method, "n_bootstrap": n_bootstrap,
                "n_success": 0, "n_failed": n_bootstrap,
                "best_fit": {}, "best_stats": best_stats,
                "wall_seconds": 0.0, "best_message": msg}

    best_fit = dict(fitted_logKs)
    best_fit.update(best_stats.get("fitted_concs",    {}) or {})
    best_fit.update(best_stats.get("fitted_titrants", {}) or {})
    all_param_names = (list(fit_keys) + list(fit_conc_keys)
                       + list(fit_titrant_keys))

    # Per-species residuals from v2 stats (preferred path).
    per_col = _extract_per_col(best_stats)
    if not per_col:
        return {"samples": {}, "ci": {}, "ci_levels": (alpha/2, 0.5, 1-alpha/2),
                "method": method, "n_bootstrap": n_bootstrap,
                "n_success": 0, "n_failed": n_bootstrap,
                "best_fit": best_fit, "best_stats": best_stats,
                "wall_seconds": time.perf_counter() - t0,
                "best_message": "No residuals reconstructible from stats"}

    # NMR-integration may map multiple columns (e.g. "GH", "GH.1") to the same
    # species in stats.  Match each species to its *canonical* (first) source
    # column so synthetic data goes into a real input column.
    col_to_sp = best_stats.get("col_to_sp") or {}
    sp_to_col_first = {}
    for c in nmr_data:
        if c.startswith("_"):
            continue
        sp = col_to_sp.get(c, c)
        if sp not in sp_to_col_first:
            sp_to_col_first[sp] = c

    def _worker(i, rng):
        try:
            synth_nmr = {col: col_data for col, col_data in nmr_data.items()}
            # Resample residuals per species, then for each species perturb
            # ALL of its source columns multiplicatively so the fit's
            # aggregation step sees the synthetic perturbation consistently.
            for sp, (resid, _yobs, y_calc) in per_col.items():
                r_synth = _resample_residuals(resid, method, rng)
                # c_synth / c_calc gives the relative perturbation per point
                ratio = np.where(np.abs(y_calc) > 1e-20,
                                 (y_calc + r_synth) / y_calc, 1.0)
                # Apply that ratio multiplicatively to every column mapping to sp
                for col, col_data in nmr_data.items():
                    if col.startswith("_"):
                        continue
                    if col_to_sp.get(col, col) != sp:
                        continue
                    y_orig = np.asarray(col_data["y"], dtype=float)
                    v_arr  = np.asarray(col_data.get("v_add_mL", []), dtype=float)
                    finite_idx = np.where(np.isfinite(v_arr) & np.isfinite(y_orig))[0]
                    k_lim = min(len(finite_idx), len(ratio))
                    y_new = y_orig.copy()
                    y_new[finite_idx[:k_lim]] = y_orig[finite_idx[:k_lim]] * ratio[:k_lim]
                    new_col = dict(col_data); new_col["y"] = y_new
                    synth_nmr[col] = new_col
            ok_b, logKs_b, stats_b, _ = fit_nmr_integration(
                parsed, network, synth_nmr, params, logK_vals, fit_keys, x_expr,
                tolerance=tolerance, maxiter=maxiter, constraints=constraints,
                fit_conc_keys=fit_conc_keys, fit_titrant_keys=fit_titrant_keys,
                compute_hessian=False,
            )
            if not ok_b:
                return None
            out = dict(logKs_b)
            out.update(stats_b.get("fitted_concs",    {}) or {})
            out.update(stats_b.get("fitted_titrants", {}) or {})
            return out
        except Exception:
            return None

    seed_seq = np.random.SeedSequence(seed)
    raw = _run_iter(_worker, (), n_bootstrap, n_jobs, progress_callback, seed_seq)
    samples, n_ok, n_fail = _finalise_samples(raw, all_param_names)
    ci_levels = (alpha/2, 0.5, 1 - alpha/2)

    return {
        "samples":     samples,
        "ci":          _ci_block(samples, alpha),
        "bs_se":       _se_block(samples),
        "ci_levels":   ci_levels,
        "method":      method,
        "n_bootstrap": n_bootstrap,
        "n_success":   n_ok,
        "n_failed":    n_fail,
        "best_fit":    best_fit,
        "best_stats":  best_stats,
        "wall_seconds": time.perf_counter() - t0,
        "best_message": msg,
    }


