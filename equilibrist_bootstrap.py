# -*- coding: utf-8 -*-
"""equilibrist_bootstrap.py

Bootstrap and Monte Carlo confidence intervals for Equilibrist fits.

The Hessian-based standard errors that ship with the fit modules assume
locally-Gaussian residuals and a locally-quadratic objective.  For
non-linear chemical models — especially with bounded parameters, weak
identifiability, or non-Gaussian noise — those assumptions can break and
the reported σ underestimates the true uncertainty.

This module supplies two model-agnostic uncertainty estimators:

  * **Non-parametric residual bootstrap** (the default).
    Residuals from the converged fit are resampled with replacement,
    added back to the model-predicted values to form a synthetic dataset,
    and the fit is repeated.  After N iterations the empirical
    percentiles of the fitted-parameter samples give the confidence
    interval.  This is the same procedure used by Bindfit
    (Thordarson et al., supramolecular.org).

  * **Parametric Monte Carlo** (optional).
    Synthetic data are generated by adding Gaussian noise to the
    model-predicted values, with σ estimated from the residuals of the
    converged fit.  This is faster and lower-variance than the
    non-parametric bootstrap but assumes the residuals are normally
    distributed (check with the Shapiro–Wilk p-value in the
    diagnostics panel before relying on it).

A "wild" bootstrap with Mammen's two-point auxiliary distribution is
also provided for heteroscedastic data.

API
---
One function per fit mode::

    bootstrap_conc      (parsed, network, exp_data, ...)
    bootstrap_nmr_shift (parsed, network, nmr_data, ...)
    bootstrap_nmr_integ (parsed, network, nmr_data, ...)
    bootstrap_spectra   (parsed, network, spectra_data, ...)
    bootstrap_kinetics  (parsed, exp_data, ...)
    bootstrap_kinetics_nmr_shifts (parsed, network, nmr_data, ...)

All return a uniform dict::

    {
      'samples':     {param_name: ndarray(n_success,)},
      'ci':          {param_name: (lo, median, hi)},
      'ci_levels':   (alpha/2, 0.5, 1-alpha/2),
      'method':      'residual' | 'parametric' | 'wild',
      'n_bootstrap': N,
      'n_success':   int,
      'n_failed':    int,
      'best_fit':    {param_name: float},   # original point estimate
      'best_stats':  dict,                  # original stats dict
      'wall_seconds': float,
      'best_message': str,
    }

Notes
-----
* All wrappers accept ``progress_callback(i, N)`` which is invoked after
  each bootstrap iteration.  Streamlit progress bars can be driven
  directly from this callback.
* ``n_jobs > 1`` triggers ``joblib.Parallel``; defaults to ``n_jobs=1``
  for stability on Streamlit Cloud and Windows multiprocessing edge
  cases.  Pass ``n_jobs=-1`` to use all cores.
* Convergence failures during the bootstrap are *silently dropped* from
  the samples.  The ``n_failed`` counter lets the caller flag this to
  the user.  If failure exceeds 20 % of N, the bootstrap should be
  considered unreliable.
* For reproducibility, pass an integer ``seed``.  Each worker is given
  a deterministic SeedSequence-derived sub-stream.

References
----------
Efron, B.; Tibshirani, R. *An Introduction to the Bootstrap*; Chapman &
Hall, 1993.
Mammen, E. *Annals of Statistics* **1993**, *21*, 255 (wild bootstrap).
Hibbert, D. B.; Thordarson, P. *Chem. Commun.* **2016**, *52*, 12792.
"""

