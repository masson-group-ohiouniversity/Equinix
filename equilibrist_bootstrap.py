            out.update(stats_b.get("fitted_concs", {}) or {})
            # Convert rate constants log → linear in the sample so the
            # MC σ reads in the same units as the displayed θ_nominal.
            for _kn in _linear_k_keys:
                if _kn in out:
                    try:
                        out[_kn] = 10.0 ** float(out[_kn])
                    except Exception:
                        pass
