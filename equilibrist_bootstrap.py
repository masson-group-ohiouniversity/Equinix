def bootstrap_conc(parsed, network, exp_data, params, logK_vals, fit_keys,
                   x_expr, *,
                   n_bootstrap: int = 1000,
                   method: str = "residual",
                   alpha: float = 0.05,
                   n_jobs: int = 1,
                   seed: Optional[int] = None,
                   tolerance: float = 1e-6,
                   maxiter: int = 100_000,
                   use_lbfgsb: bool = True,
                   use_neldermead: bool = True,
                   constraints=None,
                   fit_conc_keys=None,
                   fit_titrant_keys=None,
                   progress_callback: Optional[Callable[[int, int], None]] = None,
                   x_col_header: str = "") -> dict:
    """Bootstrap CIs for the concentration-mode fit.

    Parameters mirror :func:`equilibrist_fit_conc.fit_parameters`.  See the
    module-level docstring for the returned dict shape.
    """
    from equilibrist_fit_conc import fit_parameters
    from equilibrist_curve import (compute_single_point, find_equiv_for_x,
                                   convert_exp_x)

    t0 = time.perf_counter()
    fit_conc_keys    = list(fit_conc_keys    or [])
    fit_titrant_keys = list(fit_titrant_keys or [])

    # ── 1. Original point estimate ──────────────────────────────────────
    ok, fitted_logKs, best_stats, msg = fit_parameters(
        parsed, network, exp_data, params, logK_vals, fit_keys, x_expr,
        tolerance=tolerance, maxiter=maxiter,
        use_lbfgsb=use_lbfgsb, use_neldermead=use_neldermead,
        constraints=constraints,
        fit_conc_keys=fit_conc_keys, fit_titrant_keys=fit_titrant_keys,
    )
    if not ok:
        return {"samples": {}, "ci": {}, "ci_levels": (alpha/2, 0.5, 1-alpha/2),
                "method": method, "n_bootstrap": n_bootstrap,
                "n_success": 0, "n_failed": n_bootstrap,
                "best_fit": {}, "best_stats": best_stats,
                "wall_seconds": 0.0, "best_message": msg}

    best_fit = dict(fitted_logKs)
    fitted_concs    = best_stats.get("fitted_concs", {})    or {}
    fitted_titrants = best_stats.get("fitted_titrants", {}) or {}
    best_fit.update(fitted_concs)
    best_fit.update(fitted_titrants)
    all_param_names = (list(fit_keys)
                       + list(fit_conc_keys)
                       + list(fit_titrant_keys))

    # ── 2. Forward model with best-fit parameters → y_calc per point ───
    #     We rebuild a (v, y_obs, y_calc, col) list so each bootstrap
    #     iteration can construct synthetic y arrays without re-walking
    #     the entire forward solver per point.
    final_logKs = dict(logK_vals); final_logKs.update(fitted_logKs)
    # Optionally patch concentrations/titrants when those were fit
    if fitted_concs or fitted_titrants:
        from equilibrist_fit_conc import (_make_params_with_concs,
                                          _make_parsed_with_concs)
        fin_params = _make_params_with_concs(params, fitted_concs, fitted_titrants)
        fin_parsed = _make_parsed_with_concs(parsed, fitted_concs, fitted_titrants)
    else:
        fin_params = params
        fin_parsed = parsed

    points = []            # list of (v_add, y_obs, y_calc, exp_col)
    for exp_col, col_data in exp_data.items():
        if exp_col.startswith("_"):
            continue
        try:
            v_arr = col_data["v_add_mL"]
            y_arr = col_data["y"]
        except Exception:
            continue
        for i in range(len(v_arr)):
            v_i, y_i = float(v_arr[i]), float(y_arr[i])
            if not (np.isfinite(v_i) and np.isfinite(y_i)):
                continue
            try:
                x_val = convert_exp_x(np.array([v_i]), x_expr, fin_parsed,
                                      fin_params, network,
                                      x_col_header=x_col_header)[0]
                equiv = find_equiv_for_x(x_val, fin_parsed, fin_params)
                theo  = compute_single_point(equiv, fin_parsed, network,
                                             final_logKs, fin_params, exp_col)
            except Exception:
                continue
            if np.isfinite(theo):
                points.append((v_i, y_i, float(theo), exp_col))

    if len(points) == 0:
        return {"samples": {}, "ci": {}, "ci_levels": (alpha/2, 0.5, 1-alpha/2),
                "method": method, "n_bootstrap": n_bootstrap,
                "n_success": 0, "n_failed": n_bootstrap,
                "best_fit": best_fit, "best_stats": best_stats,
                "wall_seconds": time.perf_counter() - t0,
                "best_message": "Forward pass returned no finite points"}

    v_arr_full     = np.array([p[0] for p in points])
    y_obs_full     = np.array([p[1] for p in points])
    y_calc_full    = np.array([p[2] for p in points])
    col_labels     = [p[3] for p in points]
    residuals_full = y_obs_full - y_calc_full

    # Group point indices by column so we can substitute y per column
    col_to_idx: dict = {}
    for i, c in enumerate(col_labels):
        col_to_idx.setdefault(c, []).append(i)

    # ── 3. Bootstrap iteration ─────────────────────────────────────────
    def _worker(i, rng):
        try:
            r_synth = _resample_residuals(residuals_full, method, rng)
            y_synth = y_calc_full + r_synth
            # Build a synthetic exp_data with the same shape as the input
            synth_exp = {}
            for exp_col, col_data in exp_data.items():
                if exp_col.startswith("_"):
                    synth_exp[exp_col] = col_data
                    continue
                new_col = dict(col_data)
                v_arr_c = np.asarray(col_data["v_add_mL"], dtype=float)
                y_arr_c = np.asarray(col_data["y"],        dtype=float).copy()
                # Map (v, exp_col) → index in v_arr_full to locate the
                # synthetic y; values that did not survive the forward
                # pass are left at their original NaN-or-original value.
                idx_in_col = col_to_idx.get(exp_col, [])
                # Match by position within the column by recomputing the
                # finite-mask we used above
                finite_idx = np.where(np.isfinite(v_arr_c) & np.isfinite(y_arr_c))[0]
                # finite_idx aligns with idx_in_col in submission order;
                # forward-pass non-finite points were already dropped,
                # so finite_idx may be a superset of idx_in_col.  We
                # walk through points in order:
                k = 0
                synth_y_full = y_arr_c.copy()
                for j in finite_idx:
                    if k < len(idx_in_col):
                        synth_y_full[j] = y_synth[idx_in_col[k]]
                        k += 1
                new_col["y"] = synth_y_full
                synth_exp[exp_col] = new_col
            # Refit
            ok_b, logKs_b, stats_b, _ = fit_parameters(
                parsed, network, synth_exp, params, logK_vals, fit_keys,
                x_expr,
                tolerance=tolerance, maxiter=maxiter,
                use_lbfgsb=use_lbfgsb, use_neldermead=use_neldermead,
                constraints=constraints,
                fit_conc_keys=fit_conc_keys,
                fit_titrant_keys=fit_titrant_keys,
                compute_hessian=False,   # bootstrap doesn't need it
            )
            if not ok_b:
                return None
            out = dict(logKs_b)
            out.update(stats_b.get("fitted_concs",    {}) or {})
            out.update(stats_b.get("fitted_titrants", {}) or {})
            return out
        except Exception:
            return None

    seed_seq = np.random.SeedSequence(seed)
    raw = _run_iter(_worker, (), n_bootstrap, n_jobs, progress_callback, seed_seq)
    samples, n_ok, n_fail = _finalise_samples(raw, all_param_names)
    ci_levels = (alpha/2, 0.5, 1 - alpha/2)

    return {
        "samples":     samples,
        "ci":          _ci_block(samples, alpha),
        "bs_se":       _se_block(samples),
        "ci_levels":   ci_levels,
        "method":      method,
        "n_bootstrap": n_bootstrap,
        "n_success":   n_ok,
        "n_failed":    n_fail,
        "best_fit":    best_fit,
        "best_stats":  best_stats,
        "wall_seconds": time.perf_counter() - t0,
        "best_message": msg,
    }


