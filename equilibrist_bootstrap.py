def bootstrap_kinetics(parsed, exp_data, logk_dict, fit_keys, *,
                       t_max: float, n_pts: int,
                       n_bootstrap: int = 500,
                       method: str = "residual",
                       alpha: float = 0.05,
                       n_jobs: int = 1,
                       seed: Optional[int] = None,
                       tolerance: float = 1e-6,
                       maxiter: int = 100_000,
                       timeout_s: float = 30.0,
                       constraints=None,
                       fit_conc_keys=None,
                       progress_callback: Optional[Callable[[int, int], None]] = None
                       ) -> dict:
    """Bootstrap CIs for the kinetics fit (rate constants).

    Each iteration re-integrates the ODE system, so we keep the default
    ``n_bootstrap`` at 500 (raise it when running with ``n_jobs > 1``).
    """
    from equilibrist_kinetics import fit_kinetics, compute_kinetics_curve

    t0 = time.perf_counter()
    fit_conc_keys = list(fit_conc_keys or [])

    ok, fitted_logks, best_stats, msg = fit_kinetics(
        parsed, exp_data, logk_dict, fit_keys, t_max, n_pts,
        tolerance, maxiter, timeout_s=timeout_s,
        constraints=constraints, fit_conc_keys=fit_conc_keys,
    )
    if not ok:
        return {"samples": {}, "ci": {}, "ci_levels": (alpha/2, 0.5, 1-alpha/2),
                "method": method, "n_bootstrap": n_bootstrap,
                "n_success": 0, "n_failed": n_bootstrap,
                "best_fit": {}, "best_stats": best_stats,
                "wall_seconds": 0.0, "best_message": msg}

    best_fit = dict(fitted_logks)
    best_fit.update(best_stats.get("fitted_concs", {}) or {})
    all_param_names = list(fit_keys) + list(fit_conc_keys)

    # Run the model forward at fitted parameters to get y_calc per species
    final_logk = dict(logk_dict); final_logk.update(fitted_logks)
    sim_curve = compute_kinetics_curve(parsed, final_logk, t_max, n_pts)
    t_grid = np.asarray(sim_curve.get("t", []), dtype=float)

    # Per-species (species_name → (t_obs, y_obs, y_calc))
    per_sp: dict = {}
    for sp, col_data in exp_data.items():
        if sp.startswith("_") or sp not in sim_curve:
            continue
        try:
            t_obs = np.asarray(col_data["v_add_mL"], dtype=float)
            y_obs = np.asarray(col_data["y"],        dtype=float)
        except Exception:
            continue
        c_sp = np.asarray(sim_curve[sp], dtype=float)
        order = np.argsort(t_grid)
        y_calc = np.interp(t_obs, t_grid[order], c_sp[order])
        mask = np.isfinite(t_obs) & np.isfinite(y_obs) & np.isfinite(y_calc)
        per_sp[sp] = (t_obs[mask], y_obs[mask], y_calc[mask])

    if not per_sp:
        return {"samples": {}, "ci": {}, "ci_levels": (alpha/2, 0.5, 1-alpha/2),
                "method": method, "n_bootstrap": n_bootstrap,
                "n_success": 0, "n_failed": n_bootstrap,
                "best_fit": best_fit, "best_stats": best_stats,
                "wall_seconds": time.perf_counter() - t0,
                "best_message": "No species matched between exp_data and simulation"}

    def _worker(i, rng):
        try:
            synth_exp = {}
            for sp, col_data in exp_data.items():
                if sp.startswith("_") or sp not in per_sp:
                    synth_exp[sp] = col_data
                    continue
                t_o, y_o, y_c = per_sp[sp]
                r_synth = _resample_residuals(y_o - y_c, method, rng)
                new_col = dict(col_data)
                # Rebuild the full y array preserving any NaN slots
                v_full = np.asarray(col_data["v_add_mL"], dtype=float)
                y_full = np.asarray(col_data["y"],        dtype=float).copy()
                finite_idx = np.where(np.isfinite(v_full) & np.isfinite(y_full))[0]
                k_lim = min(len(finite_idx), len(r_synth))
                y_full[finite_idx[:k_lim]] = (y_c + r_synth)[:k_lim]
                new_col["y"] = y_full
                synth_exp[sp] = new_col
            ok_b, logks_b, stats_b, _ = fit_kinetics(
                parsed, synth_exp, logk_dict, fit_keys, t_max, n_pts,
                tolerance, maxiter, timeout_s=timeout_s,
                constraints=constraints, fit_conc_keys=fit_conc_keys,
                compute_hessian=False,
            )
            if not ok_b:
                return None
            out = dict(logks_b)
            out.update(stats_b.get("fitted_concs", {}) or {})
            return out
        except Exception:
            return None

    seed_seq = np.random.SeedSequence(seed)
    raw = _run_iter(_worker, (), n_bootstrap, n_jobs, progress_callback, seed_seq)
    samples, n_ok, n_fail = _finalise_samples(raw, all_param_names)
    ci_levels = (alpha/2, 0.5, 1 - alpha/2)

    return {
        "samples":     samples,
        "ci":          _ci_block(samples, alpha),
        "bs_se":       _se_block(samples),
        "ci_levels":   ci_levels,
        "method":      method,
        "n_bootstrap": n_bootstrap,
        "n_success":   n_ok,
        "n_failed":    n_fail,
        "best_fit":    best_fit,
        "best_stats":  best_stats,
        "wall_seconds": time.perf_counter() - t0,
        "best_message": msg,
    }


